weather = "sunny"

case weather
when "cloudy"
    puts "It's cloudy outside, you don't need your sunglasses"
when "foggy"
    puts "It's foggy outside, grab a flashlight"
when "rainy"
    puts "It's rainy outside, you're going to need your umbrella"
when "sunny"
    puts "It's sunny outside, grab your sunglasses and a hat"
else
    puts "Hmm, I don't know what you need to wear!"
end
